package ch07_ex03;
//숙제
public class Calculator {
	int num1, num2;

	
	void powerOn() { 
		System.out.println("전원을 켭니다.");
	}
		
		
	void powerOff() { 
		System.out.println("전원을 끕니다.");
	}
	
		
	int plus(int num1, int num2) {
		int plus = num1 + num2;
		return plus;
	}
	
	double divide(int num1, int num2) {
		double divide = (double)num1 / (double)num2;
		return divide;
		
	}
	
	
	
}
		

		
		
	


